import type { Metadata } from "next";
import { Analytics } from '@vercel/analytics/react';
import "./globals.css";
import { ClerkProvider, SignedIn, SignedOut, SignIn, SignInButton, UserButton } from "@clerk/nextjs";
import Navbar from "./component-custom/bars/navbar";
import { Toaster } from "@/components/ui/sonner";
import { clerkClient } from "@clerk/clerk-sdk-node";
import { signIn } from "./action";
import { auth } from "@clerk/nextjs/server";


export const metadata: Metadata = {
  title: "tripaki",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>)
{
      const { userId ,sessionId  } = await auth()
  
    try{
       if (userId) {
          const user = await clerkClient.users.getUser(userId) // âœ… No need to await clerkClient itself
      
          const email = user.emailAddresses[0]?.emailAddress
          const username = `${user.firstName ?? ''} ${user.lastName ?? ''}`.trim()
    
          let freshuser = await signIn(user.id,email,username);
       //   console.log(freshuser)
          
        }
     }catch(err){
      // console.log(err)
       if (sessionId) {
        await clerkClient.sessions.revokeSession(sessionId);
        }
      
      throw new Error(`Ops something went wrong:'${(err as Error).message}`)
      
      
     }
   
  return (
  <ClerkProvider 
  signInUrl="/sign-in"
  signUpUrl="/sign-up"
  >
    <html lang="en  " className="bg-[#010038]  ">
      <body
         className=" "
      >
        <Navbar/>
        {children}
        <Analytics />
      </body>
    </html>
  </ClerkProvider>
  );
}
